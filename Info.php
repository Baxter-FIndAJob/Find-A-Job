	<p class="heading">Reasoning</p>
	<div class="text">
		<p>Lots of teenagers in Maine want jobs. They want to make money that’s theirs to spend, and if their parents won’t pay for everything, teenagers might also want to save up for the future. While teenagers can’t make the same kind of time commitments that adults do, they can still do all sorts of things, including mow lawns, shovel snow, wash dishes and work entry level retail, just to name a few examples.</p> 

			<p>The problem is that it can be hard to understand how to go about finding and starting a job because teenagers are too young for many of the jobs that get officially listed online, and the application and permitting process can also be very confusing. Maine’s teenagers need help finding opportunities and navigating that process. </p>
	</div>

	<p class="heading"> How to Use the App </p>
	<div class="text">
		<p> Along the footer, you will see four buttons HOME, WORK PERMIT APPLICATION, ABOUT, and PROFILE.</p>
    
	<p>The HOME screen is all of the available jobs. It is a list of jobs and locations. All jobs are seeking people from 14-20 years old. You can also filter the job search in the header. </p> 

	<p>WORK PERMIT APPLICATION is a link to a work permit you can fill out.</p>

	<p>ABOUT page is where you are currently. This page outlines the apps use and tells you a little about the developers. It also provides links to important information regarding the project and the idea the sparked it.</p>

	<p>PROFILE is for business to post available jobs along with specifications. The profiles would include age, description of job, picture, name, and social media and contact information. This is not a completed section we are still working to improve both the mobile and desktop versions.</p>

